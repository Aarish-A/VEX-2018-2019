task driveTask()
{
	int THROTTLE_DZ = 10;
	int TURN_DZ = 10;
	while (true)
	{
		int throttle = (abs(gJoy[JOY_THROTTLE].cur) < THROTTLE_DZ)? 0 : gJoy[JOY_THROTTLE].cur;
		int turn = (abs(gJoy[JOY_TURN].cur) < TURN_DZ)? 0 : gJoy[JOY_TURN].cur;
		setDrive(throttle + turn, throttle - turn);
		sleep(10);
	}
}

